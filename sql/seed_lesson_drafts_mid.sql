**步骤1：移项**
x² + 6x = 7

**步骤2：配方——两边同时加上一次项系数一半的平方**
x² + 6x + 9 = 7 + 9
(x + 3)² = 16

**步骤3：开平方**
x + 3 = ±4

**步骤4：求解**
x₁ = 1, x₂ = -7

**归纳：配方法的一般步骤**

1. 移项：将常数项移到等号右边
2. 化1：二次项系数化为1（如果二次项系数不为1）
3. 配方：两边同时加上一次项系数一半的平方
4. 开方：利用平方根的意义开平方
5. 求解：解两个一元一次方程

