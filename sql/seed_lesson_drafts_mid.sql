### （三）探究活动（15分钟）

**探究：** 在△ABC和△DEF中，如果∠A = ∠D，∠B = ∠E，那么这两个三角形相似吗？

**操作验证：**
1. 画△ABC，使∠A = 50°，∠B = 60°
2. 画△DEF，使∠D = 50°，∠E = 60°
3. 测量各边的长度，计算对应边的比值

**发现：** AB/DE = BC/EF = AC/DF

**定理：** 两角分别相等的两个三角形相似（简写为"AA"）

**推理：**
∵ ∠A = ∠D，∠B = ∠E
∴ ∠C = 180° - ∠A - ∠B = 180° - ∠D - ∠E = ∠F
∴ △ABC ∽ △DEF（AA）

### （四）定理应用（12分钟）

**例题1：** 如图，在△ABC中，DE∥BC，求证：△ADE ∽ △ABC

证明：∵ DE∥BC
∴ ∠ADE = ∠B，∠AED = ∠C
又∵ ∠A = ∠A
∴ △ADE ∽ △ABC（AA）

**例题2：** 如图，在Rt△ABC中，∠C = 90°，CD⊥AB于D，找出图中所有的相似三角形

解：△ACD ∽ △ABC（∠A = ∠A，∠ADC = ∠ACB = 90°）
    △BCD ∽ △BAC（∠B = ∠B，∠BDC = ∠BCA = 90°）
    △ACD ∽ △CBD（由前两个相似可得）

